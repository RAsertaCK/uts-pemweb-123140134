export const getLocalNewsByCategory = (category) => {
  return LOCAL_NEWS_DATA[category] || LOCAL_NEWS_DATA.technology;
};

export const getLocalNewsBySearch = (query) => {
  const lowercaseQuery = query.toLowerCase();

  if (SEARCH_RESULTS[lowercaseQuery]) {
    return SEARCH_RESULTS[lowercaseQuery];
  }
 
  const allArticles = Object.values(LOCAL_NEWS_DATA).flat();
  const filteredArticles = allArticles.filter(article => 
    article.title.toLowerCase().includes(lowercaseQuery) ||
    article.description.toLowerCase().includes(lowercaseQuery)
  );
  
  if (filteredArticles.length > 0) {
    return filteredArticles;
  }

  return [
    {
      title: `Search Results for "${query}"`,
      description: `Exploring topics related to "${query}". Here are some trending technology articles.`,
      url: "#",
      urlToImage: "https://images.unsplash.com/photo-1485827404703-89b55fcc595e?w=400&h=250&fit=crop",
      publishedAt: new Date().toISOString(),
      source: { name: "News4U Search" }
    },
    ...LOCAL_NEWS_DATA.technology.slice(0, 4)
  ];
};